package com.app.model;

public class TrainingSyncPojo {
	private String training_id, trainee_names, trainee_date;

	public String getTraining_id() {
		return training_id;
	}

	public void setTraining_id(String training_id) {
		this.training_id = training_id;
	}

	public String getTrainee_names() {
		return trainee_names;
	}

	public void setTrainee_names(String trainee_names) {
		this.trainee_names = trainee_names;
	}

	public String getTrainee_date() {
		return trainee_date;
	}

	public void setTrainee_date(String trainee_date) {
		this.trainee_date = trainee_date;
	}

}
